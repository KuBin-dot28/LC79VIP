function algoV_AllBridgesAndAntiBreak(history) {
    if (history.length < 15) return null;
    const features = extractFeatures(history);
    const { runs, tx, totals } = features;
    const lastRun = runs[runs.length - 1];
    const lastTx = tx[tx.length - 1];

    // 1. Phát hiện loại cầu
    let detectedBridge = null;
    let bridgeConfidence = 0.5;

    if (lastRun && lastRun.len >= 4) {
        detectedBridge = 'cau_bet';
        bridgeConfidence = lastRun.len >= 7 ? 0.65 : (lastRun.len >= 5 ? 0.75 : 0.8);
    }
    if (!detectedBridge && runs.length >= 4) {
        const last4 = runs.slice(-4);
        if (last4.every(r => r.len === 1) && last4[0].val !== last4[1].val && last4[1].val !== last4[2].val && last4[2].val !== last4[3].val) {
            detectedBridge = 'cau_1_1';
            bridgeConfidence = 0.85;
        }
    }
    if (!detectedBridge && runs.length >= 4) {
        const last4 = runs.slice(-4);
        if (last4.every(r => r.len === 2) && last4[0].val !== last4[1].val && last4[1].val !== last4[2].val && last4[2].val !== last4[3].val) {
            detectedBridge = 'cau_2_2';
            bridgeConfidence = (lastRun.len === 2) ? 0.9 : 0.8;
        }
    }
    if (!detectedBridge && runs.length >= 4) {
        const last4 = runs.slice(-4);
        if (last4.every(r => r.len === 3) && last4[0].val !== last4[1].val && last4[1].val !== last4[2].val && last4[2].val !== last4[3].val) {
            detectedBridge = 'cau_3_3';
            bridgeConfidence = (lastRun.len === 3) ? 0.9 : 0.85;
        }
    }
    if (!detectedBridge && runs.length >= 4) {
        const lens = runs.slice(-4).map(r => r.len);
        let increasing = true;
        for (let i = 1; i < lens.length; i++) if (lens[i] <= lens[i-1]) { increasing = false; break; }
        if (increasing) { detectedBridge = 'cau_nghieng_tang'; bridgeConfidence = 0.8; }
    }
    if (!detectedBridge && runs.length >= 5) {
        const last5Lens = runs.slice(-5).map(r => r.len);
        const patternStr = last5Lens.join('');
        const patterns = {
            '123': { pred: 'alternate', conf: 0.8 },
            '321': { pred: 'alternate', conf: 0.8 },
            '131': { pred: 'continue', conf: 0.75 },
            '121': { pred: 'alternate', conf: 0.75 },
            '212': { pred: 'alternate', conf: 0.75 },
            '313': { pred: 'alternate', conf: 0.7 },
            '141': { pred: 'break', conf: 0.7 }
        };
        for (const [pat, rule] of Object.entries(patterns)) {
            if (patternStr.includes(pat)) {
                detectedBridge = `cau_nhay_${pat}`;
                bridgeConfidence = rule.conf;
                break;
            }
        }
    }
    if (!detectedBridge) {
        const localEntropy = entropy(tx.slice(-20));
        if (localEntropy < 0.4) detectedBridge = 'xu_huong_bet_nhe';
        else if (localEntropy > 0.9) detectedBridge = 'xu_huong_dao_chieu';
        else detectedBridge = 'khong_ro_cau';
    }

    // 2. Dự đoán theo cầu
    let prediction = null;
    let baseConfidence = bridgeConfidence;

    switch (detectedBridge) {
        case 'cau_bet':
            prediction = lastRun.len >= 7 ? (lastRun.val === 'T' ? 'X' : 'T') : lastRun.val;
            break;
        case 'cau_1_1':
            prediction = lastTx === 'T' ? 'X' : 'T';
            break;
        case 'cau_2_2':
            prediction = lastRun.len === 2 ? (lastRun.val === 'T' ? 'X' : 'T') : lastRun.val;
            break;
        case 'cau_3_3':
            prediction = lastRun.len === 3 ? (lastRun.val === 'T' ? 'X' : 'T') : lastRun.val;
            break;
        case 'cau_nghieng_tang':
            prediction = lastRun.len < (runs[runs.length-2]?.len || 0) + 1 ? lastRun.val : (lastRun.val === 'T' ? 'X' : 'T');
            break;
        case 'cau_nhay_123': case 'cau_nhay_321': case 'cau_nhay_121': case 'cau_nhay_212': case 'cau_nhay_313':
            prediction = lastTx === 'T' ? 'X' : 'T';
            break;
        case 'cau_nhay_131': case 'cau_nhay_141':
            prediction = lastTx === 'T' ? 'T' : 'X';
            break;
        case 'xu_huong_bet_nhe':
            prediction = lastTx; baseConfidence = 0.6;
            break;
        case 'xu_huong_dao_chieu':
            prediction = lastTx === 'T' ? 'X' : 'T'; baseConfidence = 0.6;
            break;
        default:
            const recent10 = tx.slice(-10);
            const tCount = recent10.filter(t => t === 'T').length;
            prediction = tCount >= 6 ? 'X' : (tCount <= 4 ? 'T' : (lastTx === 'T' ? 'X' : 'T'));
            baseConfidence = 0.55;
    }

    // 3. Chống gãy (phân tích rủi ro gãy cầu)
    const runLengths = runs.map(r => r.len);
    const avgRunLen = avg(runLengths);
    const maxRunLen = Math.max(...runLengths);
    const currentRunLen = lastRun ? lastRun.len : 0;
    let breakRisk = false;
    if (currentRunLen > avgRunLen * 1.8 || currentRunLen >= maxRunLen - 1) breakRisk = true;
    const recentEntropy = entropy(tx.slice(-10));
    const globalEnt = entropy(tx);
    if (recentEntropy > globalEnt + 0.2) breakRisk = true;

    // 4. Chống gãy theo hiệu suất từng thuật toán (sẽ được cập nhật bởi manager)
    // Ở đây chỉ là biến placeholder, manager sẽ truyền vào qua context nếu cần.

    let finalPrediction = breakRisk ? (prediction === 'T' ? 'X' : 'T') : prediction;
    let finalConfidence = breakRisk ? Math.max(0.6, baseConfidence * 0.9) : baseConfidence;

    return finalConfidence >= 0.55 ? finalPrediction : null;
}

