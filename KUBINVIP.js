class GodlikeEnsemble {
    constructor(algorithms, opts = {}) {
        this.algs = algorithms;
        this.weights = {};
        this.performanceHistory = {};
        this.emaAlpha = opts.emaAlpha ?? 0.04;
        this.minWeight = opts.minWeight ?? 0.005;
        this.antiBreakThreshold = 0.3; // Nếu accuracy gần đây < 0.3 thì coi là gãy

        for (const a of algorithms) {
            this.weights[a.id] = 1.0;
            this.performanceHistory[a.id] = [];
        }
    }

    fitInitial(history) {
        const window = lastN(history, Math.min(1000, history.length));
        if (window.length < 40) return;
        const algScores = {};
        for (const a of this.algs) algScores[a.id] = 0;
        const evalSamples = Math.min(60, window.length - 20);
        const startIdx = window.length - evalSamples;
        for (let i = Math.max(20, startIdx); i < window.length; i++) {
            const prefix = window.slice(0, i);
            const actual = window[i].tx;
            for (const a of this.algs) {
                try {
                    const pred = a.fn(prefix);
                    if (pred && pred === actual) algScores[a.id] += 1;
                } catch (e) {}
            }
        }
        let totalWeight = 0;
        for (const id in algScores) {
            const accuracy = algScores[id] / evalSamples;
            this.weights[id] = Math.max(this.minWeight, 0.2 + accuracy * 0.8);
            totalWeight += this.weights[id];
        }
        if (totalWeight > 0) for (const id in this.weights) this.weights[id] /= totalWeight;
    }

    updateWithOutcome(historyPrefix, actualTx) {
        if (historyPrefix.length < 15) return;
        for (const a of this.algs) {
            try {
                const pred = a.fn(historyPrefix);
                const correct = pred === actualTx ? 1 : 0;
                this.performanceHistory[a.id].push(correct);
                if (this.performanceHistory[a.id].length > 50) this.performanceHistory[a.id].shift();

                // Tính accuracy gần đây (có trọng số giảm dần)
                const recent = lastN(this.performanceHistory[a.id], 20);
                let weightedAcc = 0, weightSum = 0;
                for (let i = 0; i < recent.length; i++) {
                    const w = Math.pow(0.9, recent.length - i - 1);
                    weightedAcc += recent[i] * w;
                    weightSum += w;
                }
                const recentAccuracy = weightSum > 0 ? weightedAcc / weightSum : 0.5;

                // Nếu accuracy quá thấp -> giảm mạnh trọng số (chống gãy)
                let targetWeight = recentAccuracy;
                if (recentAccuracy < this.antiBreakThreshold) {
                    targetWeight = this.minWeight * 0.5; // gần như loại bỏ
                } else {
                    targetWeight = Math.min(1.2, recentAccuracy + 0.1);
                }

                const currentWeight = this.weights[a.id] || this.minWeight;
                const newWeight = this.emaAlpha * targetWeight + (1 - this.emaAlpha) * currentWeight;
                this.weights[a.id] = Math.max(this.minWeight, Math.min(1.8, newWeight));
            } catch (e) {
                this.weights[a.id] = Math.max(this.minWeight, (this.weights[a.id] || 1) * 0.9);
            }
        }
        const sumWeights = Object.values(this.weights).reduce((s, w) => s + w, 0);
        if (sumWeights > 0) for (const id in this.weights) this.weights[id] /= sumWeights;
    }

    predict(history) {
        if (history.length < 15) return { prediction: 'tài', confidence: 0.5, rawPrediction: 'T' };
        const votes = { T: 0, X: 0 };
        let totalWeight = 0;
        let activeCount = 0;

        for (const a of this.algs) {
            try {
                const pred = a.fn(history);
                if (!pred) continue;
                let weight = this.weights[a.id] || this.minWeight;
                votes[pred] += weight;
                totalWeight += weight;
                activeCount++;
            } catch (e) {}
        }

        if (votes.T === 0 && votes.X === 0) {
            const fallback = history[history.length-1]?.tx === 'T' ? 'X' : 'T';
            return { prediction: fallback === 'T' ? 'tài' : 'xỉu', confidence: 0.5, rawPrediction: fallback };
        }

        const { key: best } = majority(votes);
        const confidence = Math.min(0.97, votes[best] / totalWeight);
        return {
            prediction: best === 'T' ? 'tài' : 'xỉu',
            confidence,
            rawPrediction: best,
            algorithmCount: activeCount
        };
    }

    // Lấy trạng thái chống gãy cho từng thuật toán
    getAlgorithmHealth() {
        const health = {};
        for (const a of this.algs) {
            const recent = lastN(this.performanceHistory[a.id] || [], 20);
            const acc = recent.reduce((s,v)=>s+v,0) / (recent.length || 1);
            health[a.id] = {
                accuracy: acc,
                weight: this.weights[a.id] || this.minWeight,
                isBroken: acc < this.antiBreakThreshold
            };
        }
        return health;
    }
}
